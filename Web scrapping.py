import requests
from bs4 import BeautifulSoup

def get_python_latest_articles():
    url="https://www.python.org/"
    response=requests.get(url)

    if response.status_code==200:
        soup=BeautifulSoup(response.text,"html.parser")
        latest_articles=[]

        for article in soup.select(".blog-widget li"):
            title=article.a.text.strip()
            latest_articles.append(title)
        return latest_articles
    else:
        print(f"failed to retrieve data.status code:{response.status_code}")
        return []

if __name__=="__main__":
    python_articles=get_python_latest_articles()

    if python_articles:
        print("Latest article in python section:")
        for index,article in enumerate(python_articles,1):
            print(f"{index}.{article}")
    else:
        print("no article found")      
            

           
         


























































































































































































































































                 

                       